function init(hero) {
    hero.setName("Agend Perry");
    hero.setTier(1);
    
    hero.setChestplate("Suit");
    
    hero.addAttribute("JUMP_HEIGHT", 3.1, 0);
    hero.addAttribute("PUNCH_DAMAGE", 6.0, 0);
    hero.addAttribute("SPRINT_SPEED", 2.1, 1);
    hero.addAttribute("FALL_RESISTANCE", 15.5, 0);
}